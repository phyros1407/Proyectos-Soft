package beans;

public class PlanillaBean {

	private String dni;
	private int mes;
	private int ano;
	private double sueldo;
	private double aumento;
	private double descuento;
	private double seguroSalud;
	private double seguroVida;
	private double sueldoNeto;
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public double getSueldo() {
		return sueldo;
	}
	public void setSueldo(double sueldo) {
		this.sueldo = sueldo;
	}
	public double getAumento() {
		return aumento;
	}
	public void setAumento(double aumento) {
		this.aumento = aumento;
	}
	public double getDescuento() {
		return descuento;
	}
	public void setDescuento(double descuento) {
		this.descuento = descuento;
	}
	public double getSeguroSalud() {
		return seguroSalud;
	}
	public void setSeguroSalud(double seguroSalud) {
		this.seguroSalud = seguroSalud;
	}
	public double getSeguroVida() {
		return seguroVida;
	}
	public void setSeguroVida(double seguroVida) {
		this.seguroVida = seguroVida;
	}
	public double getSueldoNeto() {
		return sueldoNeto;
	}
	public void setSueldoNeto(double sueldoNeto) {
		this.sueldoNeto = sueldoNeto;
	}
	
	
	
}
