package beans;

public class ObreroBean {

	private String dni_trab;
	private double hor_ext;
	private String dia;
	
	public String getDni_trab() {
		return dni_trab;
	}
	public void setDni_trab(String dni_trab) {
		this.dni_trab = dni_trab;
	}
	public double getHor_ext() {
		return hor_ext;
	}
	public void setHor_ext(double hor_ext) {
		this.hor_ext = hor_ext;
	}
	public String getDia() {
		return dia;
	}
	public void setDia(String dia) {
		this.dia = dia;
	}
	
	
	
}
