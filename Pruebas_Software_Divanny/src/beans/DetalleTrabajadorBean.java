package beans;

public class DetalleTrabajadorBean{
	
	private String dni_trab;
	private double sueldo;
	private double afp;
	private double seg_med;
	private int dias_liq;
	private int dias_vac;
	public String getDni_trab() {
		return dni_trab;
	}
	public void setDni_trab(String dni_trab) {
		this.dni_trab = dni_trab;
	}
	public double getSueldo() {
		return sueldo;
	}
	public void setSueldo(double sueldo) {
		this.sueldo = sueldo;
	}
	public double getAfp() {
		return afp;
	}
	public void setAfp(double afp) {
		this.afp = afp;
	}
	public double getSeg_med() {
		return seg_med;
	}
	public void setSeg_med(double seg_med) {
		this.seg_med = seg_med;
	}
	public int getDias_liq() {
		return dias_liq;
	}
	public void setDias_liq(int dias_liq) {
		this.dias_liq = dias_liq;
	}
	public int getDias_vac() {
		return dias_vac;
	}
	public void setDias_vac(int dias_vac) {
		this.dias_vac = dias_vac;
	}
		
	
	
}
