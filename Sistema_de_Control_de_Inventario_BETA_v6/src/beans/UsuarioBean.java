package beans;

public class UsuarioBean {

	
		private int id;
		private String usuario;
		private String password;
		private String perfil;
		private String fecha_u_c;
		private String id_u_c;
		private String fecha_u_m;
		private String id_u_m;
		private int estado;
		
		
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getUsuario() {
			return usuario;
		}
		public void setUsuario(String usuario) {
			this.usuario = usuario;
		}
		public String getPassword() {
			return password;
		}
		public void setPassword(String password) {
			this.password = password;
		}
		public String getPerfil() {
			return perfil;
		}
		public void setPerfil(String perfil) {
			this.perfil = perfil;
		}
		public String getFecha_u_c() {
			return fecha_u_c;
		}
		public void setFecha_u_c(String fecha_u_c) {
			this.fecha_u_c = fecha_u_c;
		}
		public String getId_u_c() {
			return id_u_c;
		}
		public void setId_u_c(String id_u_c) {
			this.id_u_c = id_u_c;
		}
		public String getFecha_u_m() {
			return fecha_u_m;
		}
		public void setFecha_u_m(String fecha_u_m) {
			this.fecha_u_m = fecha_u_m;
		}
		public String getId_u_m() {
			return id_u_m;
		}
		public void setId_u_m(String id_u_m) {
			this.id_u_m = id_u_m;
		}
		
		public int getEstado() {
			return estado;
		}
		public void setEstado(int estado) {
			this.estado = estado;
		}
	
		
}
